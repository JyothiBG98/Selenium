package LoopStatement;

public class ForLoopStatement {
    public static void main(String[] args) {
        int n=10;
        for(int i=0;i<=n;i++){
            System.out.println(i);
        }
// for each statement
        String[] x={"Jyothi","hai"};
        for(String a:x){
            System.out.println(a);
        }
    }
}
